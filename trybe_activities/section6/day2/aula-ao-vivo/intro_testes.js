// os Testes Unitários testam 'uma unidade' de código
// é um teste barato, fácil de escrever, ler e alterar

// JEST
// o jest é um framework de testes muito utilizado e simples

// METODOLOGIA TDD
// Test Driven Development
// testes ao longo do desenvolvimento - vai codando, vai testando
  // fase 1: faz um teste que falhe
  // fase 2: faz o código funcionar (o teste vai passar)
  // fase 3: refatora o código para otimizar
// TESTE PRIMEIRO, DESENVOLVA DEPOIS
// constrói a estrutura de testes, do que se espera que a aplicação faça, e depois vai se escrevendo o código que passe nesses testes, que cumpra os requisitos

// npm install --save -dev jest
// linha para instalar o jest em uma aplicação
// o trecho --save-dev significa instalar somente no ambiente de desenvolvimento, ou seja, ao fazer o deploy essa ferramenta não será inclusa